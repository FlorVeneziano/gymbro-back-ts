import { DataTypes } from "sequelize";
import sequelize from "../index";

const ExerciseGuide = sequelize.define("ExerciseGuide", {
  id: {
    type: DataTypes.INTEGER,
    autoIncrement: true,
    primaryKey: true,
  },
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  description: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  image: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

export default ExerciseGuide;
